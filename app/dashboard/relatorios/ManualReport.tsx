function isoDate(d: Date): string {
  const y = d.getFullYear()
  const m = String(d.getMonth() + 1).padStart(2, '0')
  const day = String(d.getDate()).padStart(2, '0')
  return `${y}-${m}-${day}`
}

function getPresetRange(preset: string): { from: string; to: string } {
  const today = new Date()
  today.setHours(0, 0, 0, 0)

  if (preset === 'today') {
    const s = isoDate(today)
    return { from: s, to: s }
  }
  if (preset === 'yesterday') {
    const y = new Date(today)
    y.setDate(today.getDate() - 1)
    const s = isoDate(y)
    return { from: s, to: s }
  }
  if (preset === '7') {
    const s = new Date(today)
    s.setDate(today.getDate() - 6)
    return { from: isoDate(s), to: isoDate(today) }
  }
  if (preset === '30') {
    const s = new Date(today)
    s.setDate(today.getDate() - 29)
    return { from: isoDate(s), to: isoDate(today) }
  }
  if (preset === 'month') {
    const s = new Date(today.getFullYear(), today.getMonth(), 1)
    return { from: isoDate(s), to: isoDate(today) }
  }
  if (preset === 'lastMonth') {
    const s = new Date(today.getFullYear(), today.getMonth() - 1, 1)
    const e = new Date(today.getFullYear(), today.getMonth(), 0)
    return { from: isoDate(s), to: isoDate(e) }
  }
  // fallback: hoje
  const s = isoDate(today)
  return { from: s, to: s }
}

function formatCurrency(value: number, currency: string, lang: string) {
  try {
    return new Intl.NumberFormat(lang === 'en' ? 'en-US' : lang === 'es' ? 'es-ES' : 'pt-BR', {
      style: 'currency',
      currency: currency || 'BRL',
    }).format(value || 0)
  } catch {
    return String(value)
  }
}

function formatInt(value: number, lang: string) {
  return new Intl.NumberFormat(lang === 'en' ? 'en-US' : lang === 'es' ? 'es-ES' : 'pt-BR').format(value || 0)
}

