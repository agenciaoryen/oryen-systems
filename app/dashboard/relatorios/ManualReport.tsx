interface ManualReportProps {
  lang: Lang
}

export default function ManualReport({ lang }: ManualReportProps) {
  const activeOrgId = useActiveOrgId()
  const t = T[lang]

  // Range: padrão = últimos 7 dias
  const initial = getPresetRange('7')
  const [rangePreset, setRangePreset] = useState<string>('7')
  const [from, setFrom] = useState(initial.from)
  const [to, setTo] = useState(initial.to)

  // Pipeline stages da org (pra permitir seleção de "distribuição por etapa")
  const [pipelineStageIds, setPipelineStageIds] = useState<string[]>([])

  // Indicadores selecionados
  const [picked, setPicked] = useState({
    // operacional
    leads_captados: true,
    canais_origem: true,
    mensagens_enviadas: true,
    leads_responderam: true,
    // pipeline (on/off; se on, usa TODAS as stages ativas)
    pipeline: true,
    pipeline_flow: true,   // fluxo de transições dentro do período
    user_activity: true,   // atividade por responsável
    // financeiro
    receita: false,
    despesas: false,
    lucro: false,
    negocios: false,
    ticket_medio: false,
    comissoes: false,
    // site
    site_views: false,
    site_leads: false,
    site_conversion: false,
    // goals
    meta_principal: false,
  })

  const [generating, setGenerating] = useState(false)
  const [reportData, setReportData] = useState<any | null>(null)
  const [error, setError] = useState<string | null>(null)

  // Carrega pipeline_stages ativas pra incluir no relatório quando pipeline=true
  useEffect(() => {
    if (!activeOrgId) return
    supabase
      .from('pipeline_stages')
      .select('id')
      .eq('org_id', activeOrgId)
      .eq('is_active', true)
      .then(({ data }) => setPipelineStageIds((data || []).map((r: any) => r.id)))
  }, [activeOrgId])

  const handlePreset = (preset: string) => {
    setRangePreset(preset)
    if (preset === 'custom') return
    const r = getPresetRange(preset)
    setFrom(r.from)
    setTo(r.to)
  }

  const toggle = (key: keyof typeof picked) => {
    setPicked(prev => ({ ...prev, [key]: !prev[key] }))
  }

  const selectAll = () => {
    const next: any = {}
    Object.keys(picked).forEach(k => { next[k] = true })
    setPicked(next)
  }

  const clearAll = () => {
    const next: any = {}
    Object.keys(picked).forEach(k => { next[k] = false })
    setPicked(next)
  }

  const anySelected = Object.values(picked).some(Boolean)

  const handleGenerate = async () => {
    setError(null)
    if (!anySelected) {
      setError(t.noDataHint)
      return
    }
    setGenerating(true)
    try {
      const metrics = {
        base: {
          leads_captados: picked.leads_captados,
          canais_origem: picked.canais_origem,
          mensagens_enviadas: picked.mensagens_enviadas,
          leads_responderam: picked.leads_responderam,
        },
        pipeline: picked.pipeline ? pipelineStageIds : [],
        pipeline_flow: picked.pipeline_flow,
        user_activity: picked.user_activity,
        financial: {
          receita: picked.receita,
          despesas: picked.despesas,
          lucro: picked.lucro,
          negocios: picked.negocios,
          ticket_medio: picked.ticket_medio,
          comissoes: picked.comissoes,
        },
        site: {
          views: picked.site_views,
          site_leads: picked.site_leads,
          conversion: picked.site_conversion,
        },
        goals: {
          meta_principal: picked.meta_principal,
        },
        followup: {},
      }

      const res = await fetch('/api/reports/generate', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ from, to, metrics }),
      })
      const json = await res.json()
      if (!res.ok) throw new Error(json.error || t.errorGeneric)
      setReportData(json.data)
    } catch (err: any) {
      setError(err.message || t.errorGeneric)
    } finally {
      setGenerating(false)
    }
  }

  const handlePrint = () => {
    if (!reportData) return
    const printWindow = window.open('', '_blank')
    if (!printWindow) return
    printWindow.document.write(buildPrintHtml(reportData, t, lang))
    printWindow.document.close()
    // Pequeno delay pro browser renderizar antes de imprimir
    setTimeout(() => printWindow.print(), 300)
  }

  return (
    <div className="rounded-2xl p-5 sm:p-6" style={{ background: 'var(--color-bg-elevated)', border: '1px solid var(--color-border)' }}>
      {/* Header */}
      <div className="flex items-start gap-3 mb-5">
        <div className="p-2.5 rounded-xl shrink-0" style={{ background: 'var(--color-primary-subtle)', color: 'var(--color-primary)' }}>
          <FileText size={20} />
        </div>
        <div>
          <h2 className="text-lg font-bold" style={{ color: 'var(--color-text-primary)' }}>{t.title}</h2>
          <p className="text-sm mt-0.5" style={{ color: 'var(--color-text-secondary)' }}>{t.subtitle}</p>
        </div>
      </div>

      {/* Controles */}
      <div className="grid grid-cols-1 md:grid-cols-3 gap-3 mb-5">
        <div>
          <label className="block text-xs font-semibold mb-1.5" style={{ color: 'var(--color-text-secondary)' }}>{t.quickRange}</label>
          <CustomSelect
            value={rangePreset}
            onChange={handlePreset}
            options={[
              { value: 'today', label: t.rangeToday },
              { value: 'yesterday', label: t.rangeYesterday },
              { value: '7', label: t.range7 },
              { value: '30', label: t.range30 },
              { value: 'month', label: t.rangeMonth },
              { value: 'lastMonth', label: t.rangeLastMonth },
              { value: 'custom', label: t.rangeCustom },
            ]}
          />
        </div>
        <div>
          <label className="block text-xs font-semibold mb-1.5" style={{ color: 'var(--color-text-secondary)' }}>{t.from}</label>
          <input
            type="date"
            value={from}
            onChange={e => { setFrom(e.target.value); setRangePreset('custom') }}
            className="w-full px-3 py-2.5 rounded-lg text-sm outline-none"
            style={{ background: 'var(--color-bg-base)', border: '1px solid var(--color-border)', color: 'var(--color-text-primary)' }}
          />
        </div>
        <div>
          <label className="block text-xs font-semibold mb-1.5" style={{ color: 'var(--color-text-secondary)' }}>{t.to}</label>
          <input
            type="date"
            value={to}
            onChange={e => { setTo(e.target.value); setRangePreset('custom') }}
            className="w-full px-3 py-2.5 rounded-lg text-sm outline-none"
            style={{ background: 'var(--color-bg-base)', border: '1px solid var(--color-border)', color: 'var(--color-text-primary)' }}
          />
        </div>
      </div>

      {/* Indicadores */}
      <div className="mb-5">
        <div className="flex items-center justify-between mb-3">
          <label className="text-sm font-bold" style={{ color: 'var(--color-text-primary)' }}>{t.indicators}</label>
          <div className="flex gap-2">
            <button onClick={selectAll} className="text-xs font-medium px-2 py-1 rounded" style={{ color: 'var(--color-primary)', background: 'var(--color-primary-subtle)' }}>{t.selectAll}</button>
            <button onClick={clearAll} className="text-xs font-medium px-2 py-1 rounded" style={{ color: 'var(--color-text-secondary)', background: 'var(--color-bg-hover)', border: '1px solid var(--color-border)' }}>{t.clearAll}</button>
          </div>
        </div>

        <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
          <CategoryBlock title={t.operationalCat}>
            <Chk label={t.m_leads_captados} checked={picked.leads_captados} onToggle={() => toggle('leads_captados')} />
            <Chk label={t.m_canais_origem} checked={picked.canais_origem} onToggle={() => toggle('canais_origem')} />
            <Chk label={t.m_mensagens_enviadas} checked={picked.mensagens_enviadas} onToggle={() => toggle('mensagens_enviadas')} />
            <Chk label={t.m_leads_responderam} checked={picked.leads_responderam} onToggle={() => toggle('leads_responderam')} />
          </CategoryBlock>

          <CategoryBlock title={t.pipelineCat}>
            <Chk label={t.m_pipeline} checked={picked.pipeline} onToggle={() => toggle('pipeline')} />
            <Chk label={t.m_pipeline_flow} checked={picked.pipeline_flow} onToggle={() => toggle('pipeline_flow')} />
            <Chk label={t.m_user_activity} checked={picked.user_activity} onToggle={() => toggle('user_activity')} />
          </CategoryBlock>

          <CategoryBlock title={t.financialCat}>
            <Chk label={t.m_receita} checked={picked.receita} onToggle={() => toggle('receita')} />
            <Chk label={t.m_despesas} checked={picked.despesas} onToggle={() => toggle('despesas')} />
            <Chk label={t.m_lucro} checked={picked.lucro} onToggle={() => toggle('lucro')} />
            <Chk label={t.m_negocios} checked={picked.negocios} onToggle={() => toggle('negocios')} />
            <Chk label={t.m_ticket_medio} checked={picked.ticket_medio} onToggle={() => toggle('ticket_medio')} />
            <Chk label={t.m_comissoes} checked={picked.comissoes} onToggle={() => toggle('comissoes')} />
          </CategoryBlock>

          <CategoryBlock title={t.siteCat}>
            <Chk label={t.m_site_views} checked={picked.site_views} onToggle={() => toggle('site_views')} />
            <Chk label={t.m_site_leads} checked={picked.site_leads} onToggle={() => toggle('site_leads')} />
            <Chk label={t.m_site_conversion} checked={picked.site_conversion} onToggle={() => toggle('site_conversion')} />
          </CategoryBlock>

          <CategoryBlock title={t.goalsCat}>
            <Chk label={t.m_meta_principal} checked={picked.meta_principal} onToggle={() => toggle('meta_principal')} />
          </CategoryBlock>
        </div>
      </div>

      {error && (
        <p className="text-sm mb-3" style={{ color: 'var(--color-error)' }}>{error}</p>
      )}

      {/* Ação de gerar */}
      <button
        onClick={handleGenerate}
        disabled={generating}
        className="w-full flex items-center justify-center gap-2 px-5 py-3 rounded-xl text-sm font-bold transition-all disabled:opacity-60 shadow-sm"
        style={{ background: 'var(--color-primary)', color: '#fff' }}
      >
        {generating ? (
          <>
            <Loader2 size={16} className="animate-spin" />
            {t.generating}
          </>
        ) : (
          <>
            <FileText size={16} />
            {t.generate}
          </>
        )}
      </button>

      {/* Preview */}
      {reportData && (
        <div className="mt-5 rounded-xl p-4 sm:p-5" style={{ background: 'var(--color-bg-base)', border: '1px solid var(--color-border)' }}>
          <div className="flex items-center justify-between mb-3">
            <h3 className="text-sm font-bold" style={{ color: 'var(--color-text-primary)' }}>{t.previewTitle}</h3>
            <div className="flex gap-2">
              <button
                onClick={handlePrint}
                className="flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-xs font-semibold transition-colors"
                style={{ background: 'var(--color-success-subtle)', color: 'var(--color-success)', border: '1px solid var(--color-success)' }}
              >
                <Download size={12} />
                {t.print}
              </button>
              <button
                onClick={() => setReportData(null)}
                className="flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-xs font-medium"
                style={{ background: 'var(--color-bg-hover)', color: 'var(--color-text-secondary)', border: '1px solid var(--color-border)' }}
              >
                {t.closePreview}
              </button>
            </div>
          </div>

          <ReportPreview data={reportData} t={t} lang={lang} />
        </div>
      )}
    </div>
  )
}

